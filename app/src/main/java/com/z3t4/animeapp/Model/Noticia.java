package com.z3t4.animeapp.Model;

public class Noticia {
    String mTitulo, mCreador, mFecha, mLink, mDescripcion, mThumb;

    public String getmTitulo() {
        return mTitulo;
    }

    public void setmTitulo(String mTitulo) {
        this.mTitulo = mTitulo;
    }

    public String getmCreador() {
        return mCreador;
    }

    public void setmCreador(String mCreador) {
        this.mCreador = mCreador;
    }

    public String getmFecha() {
        return mFecha;
    }

    public void setmFecha(String mFecha) {
        this.mFecha = mFecha;
    }

    public String getmLink() {
        return mLink;
    }

    public void setmLink(String mLink) {
        this.mLink = mLink;
    }

    public void setmDescripcion(String mDescripcion) {
        this.mDescripcion = mDescripcion;
    }

    public String getmDescripcion() {
        return mDescripcion;
    }

    public String getmThumb() { return mThumb; }

    public void setmThumb(String mThumb) { this.mThumb = mThumb; }
}


